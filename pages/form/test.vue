<template>
  <section class="index-page">
    <h2>Sign In</h2>

    <form @submit.prevent="onSubmit">
      <input
        autocomplete="true"
        placeholder="Email"
        type="email"
        v-model="email"
      >
      
      <input
        autocomplete="current-password"
        placeholder="Password"
        type="password"
        v-model="password"
      >
      
      <button type="submit">Sign In</button>
    </form>
    <div class="g-recaptcha" data-sitekey="6Lf0pMwcAAAAACGnNa6nqBVATAudJT0VJki6kHP-"></div>
        <br/>
  </section>
</template>

<script>
export default {
  data: () => ({
    email: 'test@example.com',
    password: '123',
  }),
  async mounted() {
    try {
      await this.$recaptcha.init()
    } catch (e) {
      console.log(e);
    }
  },
  methods: {
    async onSubmit() {
      try {
        const token = await this.$recaptcha.execute('login')
        console.log('ReCaptcha token:', token)
      } catch (error) {
        console.log('Login error:', error)
      }
    },
  },
}
</script>